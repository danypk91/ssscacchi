const DIR = resolve('./src/content/notizie');
const files = readdirSync(DIR).filter(f => f.endsWith('.md'));

let updated = 0;
let unchanged = 0;

for (const file of files) {
  // Extract ID from filename (e.g., "480-trofeo-..." -> 480)
  const idMatch = file.match(/^(\d+)-/);
  if (!idMatch) continue;
  const id = parseInt(idMatch[1]);
  const correctDate = DATES[id];
  if (!correctDate) {
    console.warn(`No date for ID ${id} (${file})`);
    continue;
  }

  const path = resolve(DIR, file);
  const content = readFileSync(path, 'utf8');
  const currentDateMatch = content.match(/date: "([^"]+)"/);
  if (!currentDateMatch) continue;

  if (currentDateMatch[1] === correctDate) {
    unchanged++;
    continue;
  }

  const newContent = content.replace(`date: "${currentDateMatch[1]}"`, `date: "${correctDate}"`);
  writeFileSync(path, newContent);
  updated++;
}

console.log(`Updated: ${updated}, Unchanged: ${unchanged}, Total: ${files.length}`);